/* Exercício 4
Crie uma variável inicializada com um valor de qualquer tipo e, em seguida, reatribua um novo valor a essa variável.
Imprima ambos os valores no console após atribuí-los.*/

// Declara uma variável chamada 'var1' usando 'let' e atribui o valor numérico 10 a ela.
let var1 = 10;

// Reatribui o valor da variável 'var1' para a string "Thomazoni".
// Isso demonstra que 'let' permite alterar o valor de uma variável após a declaração.
var1 = "Thomazoni";

// Imprime o valor atual da variável 'var1' no console.
// Como 'var1' foi reatribuída, o valor impresso será "Thomazoni".
console.log(var1);

